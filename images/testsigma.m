clear;
close all;
clc;
u=0.5;
a0=5;
a1=4;
a2=7;
w1=2;
w2=10;
k1=30;
k2=9;
x=-10:0.01:20;
y1=zeros(1,length(x));
p1=k1.*(35-x).*sin(w1.*(x-a1));
p2=k2.*x.*cos(w2.*(x-a2));
y1(x>a0)=p1(x>a0);
y1(x<=a0)=p2(x<=a0);
s=1./(1+exp(-u.*(x-a0)));
y2=s.*p1+(1-s).*p2;
figure;
plotyy(x,[y1;y2],x,s);
legend('y1','y2','s');
figure;
plotyy(x,[y1-y2],x,s);
legend('y1-y2','s');
